package org.example.operators;

public class ArithmeticExample {

    public static void main ( String[] args ) {
        // Declare two integer variables
        int num1 = 20;
        int num2 = 6;

        // Addition (+)
        int sum = num1 + num2;
        System.out.println("Addition (num1 + num2): " + sum);  // Output: 26
    }
}
